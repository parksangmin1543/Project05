package method.quiz;

public class Quiz2 {
	public static void minus(int a, int b) {
		System.out.println(a - b);
	}
	public static void mul(int a, int b) {
		System.out.println(a * b);
	}
	public static void div(int a, int b) {
		System.out.println(a / b);
	}
	
	public static void main(String[] args) {
		minus(8, 3);
		mul(10, 5);
		div(10, 2);
	}
}
